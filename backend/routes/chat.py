from fastapi import APIRouter
from pydantic import BaseModel
import re
import os
import google.generativeai as genai
from dotenv import load_dotenv

load_dotenv()

router = APIRouter()

class ChatMessage(BaseModel):
    role: str
    content: str

class ChatRequest(BaseModel):
    message: str
    history: list[ChatMessage] = []

# ── Gemini setup ───────────────────────────────────────────────────
GEMINI_API_KEY = os.getenv("GEMINI_API_KEY", "")
_gemini_model = None

if GEMINI_API_KEY:
    try:
        genai.configure(api_key=GEMINI_API_KEY)
        _gemini_model = genai.GenerativeModel("gemini-2.0-flash")
    except Exception as e:
        print(f"Gemini init failed: {e}")
else:
    print("[Warning] GEMINI_API_KEY not set. Chatbot will use local rule-based responses.")

GEMINI_SYSTEM_PROMPT = """तुम्ही AgroVision AI आहात — एक कांदा शेती तज्ञ सहायक.
तुम्ही फक्त **मराठी भाषेत** उत्तर द्यावे.
उत्तर साधे, स्पष्ट आणि शेतकऱ्यांना समजेल असे असावे.
उत्तर ३-५ वाक्यात द्या. रोगाचे नाव, लक्षणे, उपचार आणि प्रतिबंध यांचा उल्लेख करा.
फक्त कांदा शेती, रोग, कीटक, खत आणि शेतीशी संबंधित विषयांवर उत्तर द्या."""


async def ask_gemini(message: str, history: list[ChatMessage] = None) -> str | None:
    """Try Gemini API. Returns reply string or None on failure."""
    if not _gemini_model:
        return None
    try:
        # Build conversation context
        context = ""
        if history:
            for msg in history[-4:]:  # Use last 4 messages for context
                context += f"{msg.role}: {msg.content}\n"
        
        prompt = f"{GEMINI_SYSTEM_PROMPT}\n\n{context}\nशेतकऱ्याचा प्रश्न: {message}\n\nउत्तर:"
        response = _gemini_model.generate_content(prompt)
        reply = response.text.strip()
        return reply if reply else None
    except Exception as e:
        print(f"[Gemini] API call failed: {e}. Falling back to local answers.")
        return None


